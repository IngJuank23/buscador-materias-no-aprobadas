@app.route('/', methods=['GET', 'POST'])
def index():
    resultados = []
    mensaje = ""

    # 🧮 Contador de visitas usando SQLite
    try:
        conn = sqlite3.connect("visitas.db")
        cursor = conn.cursor()

        cursor.execute("SELECT contador FROM visitas LIMIT 1")
        row = cursor.fetchone()

        if row:
            contador = row[0] + 1
            cursor.execute("UPDATE visitas SET contador = ?", (contador,))
        else:
            contador = 1
            cursor.execute("INSERT INTO visitas (contador) VALUES (?)", (contador,))

        conn.commit()
        conn.close()

    except Exception as e:
        print("Error en contador de visitas:", e)
        contador = "?"

    if request.method == 'POST':
        texto = request.form['materias']
        lineas = texto.strip().split('\n')

        for entrada in lineas:
            entrada_normalizada = unidecode(entrada.strip().lower())
            if not entrada_normalizada:
                continue

            encontrado = False
            for clave, datos in materias.items():
                nombre_normalizado = unidecode(datos['nombre'].lower())
                if nombre_normalizado == entrada_normalizada:
                    resultados.append({
                        'nombre': datos['nombre'],
                        'clave': clave,
                        'nivelacion': datos['nivelacion']
                    })
                    encontrado = True
                    break

            if not encontrado:
                resultados.append({
                    'nombre': entrada.strip().title(),
                    'clave': '',
                    'nivelacion': ''
                })

        if not resultados:
            mensaje = "No se ingresaron materias válidas."

    return render_template('index.html', resultados=resultados, mensaje=mensaje, contador=contador)

